// Regenerates lib/favorite-tags.ts by fetching a curated list of tag IDs from
// barbershoptags.com ONCE and baking the parsed metadata into the app, so these
// "favorite" tags load instantly without hitting the slow public API at runtime.
//
// To add/remove favorites: edit FAVORITE_IDS below, then run:
//   node scripts/gen-favorites.mjs
//
// The parser + mapTag logic here mirrors lib/tags-api.ts so the baked objects
// are equivalent to what the live app's mapTag produces (identical after JSON
// serialization). If mapTag in lib/tags-api.ts changes, update this to match.

import { XMLParser } from 'fast-xml-parser';
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

// Curated favorites, in display order. Comment is just a human label.
const FAVORITE_IDS = [
  1652, // If Only For A Day
  2128, // The Dream Tag
  2275, // Like Leaves We'll Fall In Love (Jake Tickner, 4-part)
  20, //   When I Leave the World Behind
];

const BASE = 'https://www.barbershoptags.com/api.php';

const parser = new XMLParser({
  ignoreAttributes: false,
  attributeNamePrefix: '@_',
  textNodeName: '#text',
  parseAttributeValue: false,
  processEntities: true,
  htmlEntities: true,
});

function extractUrl(field) {
  if (!field) return null;
  if (typeof field === 'string') return field.trim() || null;
  if (typeof field === 'object' && field !== null) {
    const v = field['#text'];
    return v?.trim() || null;
  }
  return null;
}

function extractType(field) {
  if (!field || typeof field !== 'object') return null;
  const t = field['@_type'];
  return t ? t.toLowerCase() : null;
}

function toNum(v, fallback = 0) {
  const n = typeof v === 'string' ? parseFloat(v) : typeof v === 'number' ? v : NaN;
  return Number.isFinite(n) ? n : fallback;
}

function toStr(v) {
  if (v == null) return '';
  if (typeof v === 'string') return v;
  if (typeof v === 'number') return String(v);
  if (typeof v === 'object') return '';
  return String(v);
}

function mapTag(raw) {
  const videos = raw.videos?.video
    ? Array.isArray(raw.videos.video)
      ? raw.videos.video
      : [raw.videos.video]
    : [];
  const firstVideo = videos[0];
  const voiceTracks = {};
  const tenor = extractUrl(raw.Tenor);
  const lead = extractUrl(raw.Lead);
  const bari = extractUrl(raw.Bari);
  const bass = extractUrl(raw.Bass);
  if (tenor) voiceTracks.Tenor = tenor;
  if (lead) voiceTracks.Lead = lead;
  if (bari) voiceTracks.Bari = bari;
  if (bass) voiceTracks.Bass = bass;
  return {
    id: toNum(raw.id),
    title: toStr(raw.Title),
    altTitle: toStr(raw.AltTitle),
    arranger: toStr(raw.Arranger),
    parts: toNum(raw.Parts, 4),
    type: toStr(raw.Type),
    writKey: toStr(raw.WritKey),
    rating: toNum(raw.Rating),
    ratingCount: toNum(raw.RatingCount),
    downloaded: toNum(raw.Downloaded),
    notes: toStr(raw.Notes),
    lyrics: toStr(raw.Lyrics),
    sheetMusic: extractUrl(raw.SheetMusic),
    sheetMusicType: extractType(raw.SheetMusic),
    sheetMusicAlt: toStr(raw.SheetMusicAlt) || null,
    voiceTracks,
    allParts: extractUrl(raw.AllParts),
    notation: extractUrl(raw.Notation),
    notationAlt: toStr(raw.NotationAlt) || null,
    videoCode: firstVideo?.Code ? String(firstVideo.Code) : null,
  };
}

async function fetchTagById(id) {
  const params = new URLSearchParams({ id: String(id), client: 'barbershop-night' });
  const res = await fetch(`${BASE}?${params.toString()}`);
  if (!res.ok) throw new Error(`tag ${id}: HTTP ${res.status}`);
  const xml = await res.text();
  const parsed = parser.parse(xml);
  const tagsNode = parsed?.tags?.tag;
  if (!tagsNode) throw new Error(`tag ${id}: not found in response`);
  const raw = Array.isArray(tagsNode) ? tagsNode[0] : tagsNode;
  return mapTag(raw);
}

const tags = [];
for (const id of FAVORITE_IDS) {
  process.stdout.write(`fetching tag ${id}… `);
  const tag = await fetchTagById(id);
  if (tag.id <= 0) throw new Error(`tag ${id}: parsed id was ${tag.id}`);
  console.log(`ok — "${tag.title}"`);
  tags.push(tag);
}

const header = `// AUTO-GENERATED by scripts/gen-favorites.mjs — do not edit by hand.
// Pre-loaded ("favorite") tags baked in so they render instantly without the
// slow barbershoptags.com API. To change the list, edit FAVORITE_IDS in
// scripts/gen-favorites.mjs and re-run \`node scripts/gen-favorites.mjs\`.
import type { Tag } from './types';

export const FAVORITE_TAGS: Tag[] = ${JSON.stringify(tags, null, 2)};

export const FAVORITE_TAG_IDS = new Set<number>(FAVORITE_TAGS.map((t) => t.id));

export function getFavoriteTag(id: number): Tag | undefined {
  return FAVORITE_TAGS.find((t) => t.id === id);
}
`;

const outPath = join(dirname(fileURLToPath(import.meta.url)), '..', 'lib', 'favorite-tags.ts');
await writeFile(outPath, header, 'utf8');
console.log(`\nwrote ${tags.length} tags -> lib/favorite-tags.ts`);
